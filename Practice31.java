public class Practice31 
{
    public int theMaximumAchievableX(int num, int t) {
        return num + 2 * t;
    }

    public static void main(String[] args) {
        Practice31 sol = new Practice31();

        
        System.out.println(sol.theMaximumAchievableX(4, 1));  // Output: 6
        
    }
}


                         

